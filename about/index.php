                <div class="wrap">
                    <h3>Frequently Asked Questions</h3>

                    <details>
                        <summary>What are the Rules?</summary>

                        <div class="answer">
                            <p>We currently adhere to the USAT Rules for Triathlon & Duathlon. Important rules include no drafting, you must wear a helmet and music is ABSOLUTELY NOT allowed during the bike for obvious safety reasons.</p>
                        </div>
                    </details>

                    <details>
                        <summary>Can I use a personal music device while cycling?</summary>

                        <div class="answer">
                            <p>Absolutely no music devices are allowed during the bike segment for obvious safety risks and will result in immediate disqualification.</p>
                        </div>
                    </details>

                    <details>
                        <summary>Can I use a personal music device while running?</summary>

                        <div class="answer">
                            <p>During any running segments, we prefer for athletes to NOT use music devices to maximize safety, assure a fair competitive environment and assure athletes can hear all course marshal instructions.</p>

                            <p>With that said, we also understand that many athletes rely on music to help endure the challenge of running. To accommodate the needs of our athletes while still assuring maximum safety and a fair, competitive environment, here is the arrangement that we can accommodate:</p>

                            <ul class="default-ul">
                                <li>If you chose to use a personal music device, you must always keep one ear open for instructions so only one ear-bud can be used at any time. In addition, if you opt to use music, you will not be eligible for awards, points and rankings. So as an athlete, you have to make a choice - music or awards/points/rankings.</li>

                                <li>If you are using a personal music devise with two earbuds, you will be immediately disqualified. Trust us, we don't want to have to enforce a disqualification but in order to assure the safety of all athletes, this is a very important rule.</li>

                                <li>We also request that if you chose to use a personal music device that you keep the volume to a minimum to assure all instructions can be heard.</li>
                            </ul>
                        </div>
                    </details>

                    <details>
                        <summary>Do I need to wear a wetsuit?</summary>

                        <div class="answer">
                            <p>No, you do not need to wear a wetsuit. Many will not wear a wetsuit while others will choose to wear a wetsuit because of the buoyancy and warmth factor.</p>
                        </div>
                    </details>

                    <details>
                        <summary>Do I have to use a road or racing bike?</summary>

                        <div class="answer">
                            <p>No. We welcome any type of bike as long as it is functioning properly with brakes and endcaps at the end of your handlebars.</p>
                        </div>
                    </details>
